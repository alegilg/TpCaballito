package TpCaballito;

public class Vector {
    int x;
    int y;
    int initialPositionX = 2;
    int initialPositionY = 1;

    public Vector(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
